export type Theme = keyof typeof topics;

export function getRandomTopics(theme: Theme, count: number = 3): string[] {
  if (!topics[theme]) {
    throw new Error(
      `Invalid theme: ${theme}. Available themes: ${Object.keys(topics).join(
        ", "
      )}`
    );
  }

  const allTopics = topics[theme];
  const shuffled = [...allTopics].sort(() => 0.5 - Math.random());
  return shuffled.slice(0, count);
}
